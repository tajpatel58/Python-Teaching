

def square_number(x: float) -> float:
    return x ** 2

def cube_number(x: float) -> float:
    return x ** 3

def area(length: int, width: int) -> int:
    return length * width

def perimeter(length: int, width: int) -> int:
    return 2 * (length + width)

def add(x: int, y: int) -> int:
    return x + y
